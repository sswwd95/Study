import numpy as np
import matplotlib.pyplot as plt


def Leaky_ReLU(x):
    return np.maximum(0.01*x, x) # 0보다 작으면 0이고 0보다 크면 x값으로 한다.

x = np.arange(-5,5,0.1)
y = Leaky_ReLU(x)

print(x)
print(y)

plt.plot(x,y)
plt.grid()
plt.show()

